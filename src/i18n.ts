export type Language = keyof typeof messages;
export type TranslationKey = keyof (typeof messages)["en"];

type ReplacementValue = number | string;

export const language: Language = detectSystemLanguage();
export const locale = language === "pl" ? "pl-PL" : "en-US";

export function t(key: TranslationKey, replacements: Record<string, ReplacementValue> = {}): string {
  let message: string = messages[language][key];
  for (const [name, value] of Object.entries(replacements)) {
    message = message.replaceAll(`{${name}}`, String(value));
  }
  return message;
}

function detectSystemLanguage(): Language {
  const languages = navigator.languages.length > 0 ? navigator.languages : [navigator.language];
  return languages.some((value) => value.toLowerCase().startsWith("pl")) ? "pl" : "en";
}
